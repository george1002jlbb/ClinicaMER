    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        List l = new LinkedList();

        try {
            l.clear(); // limpiamos el listado
            l = logica.consultarHistoriaMed( cboHis.getItemAt(cboHis.getSelectedIndex()).getIdCombo() );
            //System.out.println("lista " + l.size());
            DefaultTableModel modelo = new DefaultTableModel();
            jtblBusqEnf.setModel(modelo);
            modelo.addColumn("Historia Clinica");
            modelo.addColumn("Enfermedad");

            jLabel.setText("Resultado (" + l.size() + ")");
            Object[] fila = null;
            for (int i = 0; i < l.size(); i++) {
                modelo.addRow(fila);
                // Se crea un array que serÃ¡ una de las filas de la tabla.
                HistoriaMed hm = (HistoriaMed) l.get(i);
                //fila[i] = l.get(i);
                modelo.setValueAt(hm.getIdhistoria(), i, 0);
                modelo.setValueAt( logica.consultarMedicamentoId( hm.getIdmedicamento() ).getNombre(), i, 1);
            }
        } catch (Exception ex) {
            Logger.getLogger(frmCHistoriaMed.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnEnviarActionPerformed

