    public void consultarCitas() throws IOException, IllegalStateException, NoSuchAlgorithmException, Exception {
        List l = new LinkedList();
        l.clear(); // limpiamos el listado
        l = logica.listarTodosCitas();

        DefaultTableModel modelo = new DefaultTableModel();
        jtblListadoConsultas.setModel(modelo);
        modelo.addColumn("Nro Cita");
        modelo.addColumn("Paciente");
        modelo.addColumn("Medico");
        modelo.addColumn("Especialidad");
        modelo.addColumn("Fecha");
        modelo.addColumn("Estado");
        //jLabel3.setText("Resultado (" + l.size() + ")");
        Object[] fila = null;
        for (int i = 0; i < l.size(); i++) {
            modelo.addRow(fila);
            // Se crea un array que serÃ¡ una de las filas de la tabla.
            Consulta c = (Consulta) l.get(i);
            System.out.println("consulta " + c.toString());
            //fila[i] = l.get(i);
            modelo.setValueAt(c.getIdconsulta(), i, 0);
            modelo.setValueAt(logica.consultarPacienteId(c.getIdpaciente()).getNombre() + " " + logica.consultarPacienteId(c.getIdpaciente()).getApellido(), i, 1);
            modelo.setValueAt(logica.consultarMedicoId(c.getIdmedico()).getNombre() + " " + logica.consultarMedicoId(c.getIdmedico()).getApellido(), i, 2);
            modelo.setValueAt(c.getEspecialidad(), i, 3);
            modelo.setValueAt(c.getFeconsulta(), i, 4);
            modelo.setValueAt(c.getEstado(), i, 5);

        }

    }

